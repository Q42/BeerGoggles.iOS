import UIKit

struct Fonts {
  static func futuraMedium(with size: CGFloat) -> UIFont? {
    return UIFont(name: "Futura-Medium", size: size)
  }

  static func futuraBold(with size: CGFloat) -> UIFont? {
    return UIFont(name: "Futura-Bold", size: size)
  }
}
